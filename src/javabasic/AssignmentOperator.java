package javaPractice;

public class AssignmentOperator {

	public static void main(String[] args) {

		// Minimizing code!

		System.out.println("Minimizing code Example..........");
		System.out.println();
		
		int x = 10;
		x += 5;

		System.out.println("After adding value of y is " + x);

		x -= 5;
		System.out.println("After subtracting value of y is " + x);

		x *= 5;
		System.out.println("After multiplying value of y is " + x);

		x /= 5;
		System.out.println("After dividing value of y is " + x);

		x %= 4;
		System.out.println("Remainder value of y is " + x);
	}
}